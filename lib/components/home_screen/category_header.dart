import 'package:flutter/material.dart';

class CategoryHeader extends StatelessWidget {

  // -------------------------------------------------------------------------------------------------
  // attributes
  // -------------------------------------------------------------------------------------------------

  int id;
  String name;

  // -------------------------------------------------------------------------------------------------
  // constructor & factories
  // -------------------------------------------------------------------------------------------------

  CategoryHeader(this.id, this.name);

  // -------------------------------------------------------------------------------------------------
  // build
  // -------------------------------------------------------------------------------------------------

  @override
  Widget build(BuildContext context) {
    return ListTile(
      title: Text(name),
      leading: Icon(Icons.face),
    );
  }
}
